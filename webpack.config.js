/* eslint global-require: "off" */
const webpack = require('webpack');

const DEBUG = 'DEBUG' in process.env && parseInt(process.env.DEBUG, 10) > 0;
const PROD = process.argv.indexOf('-p') !== -1;
const NODE_ENV = PROD ? 'production' : 'development';
const USE_SOURCE_MAP = DEBUG;
const USE_LINTERS = DEBUG;

console.log(`Enviroment: ${NODE_ENV}`);
console.log(`Debug: ${DEBUG ? 'enabled' : 'disabled'}`);
console.log(`Linters: ${USE_LINTERS ? 'enabled' : 'disabled'}`);
console.log(`Source maps: ${USE_SOURCE_MAP ? 'enabled' : 'disabled'}`);
console.log('---\nWebpack running...');

const StyleLintPlugin = require('stylelint-webpack-plugin');
const ExtractTextPlugin = require('extract-text-webpack-plugin');

const banner = new String(''); // eslint-disable-line no-new-wrappers
banner.toString = () => {
    return `Generated by Intecmedia.Webpack: ${new Date().toISOString()} | ${NODE_ENV} | [file]?v=[chunkhash]`;
};

module.exports = {

    entry: {
        vendor: [
            'jquery',
            './app/vendor.js',
        ],
        app: './app/app.js',
    },

    output: {
        filename: './assets/app.min.js',
    },

    performance: {
        hints: PROD && !DEBUG ? 'error' : false,
        maxAssetSize: 512 * 1024,
        maxEntrypointSize: 256 * 1024,
    },

    plugins: (PROD ? [
        // prod-only
        new webpack.EnvironmentPlugin({
            NODE_ENV: 'production',
            DEBUG: false,
        }),
        new webpack.DefinePlugin({
            'NODE_ENV': JSON.stringify('production'),
            'process.env': {
                NODE_ENV: 'production',
            },
        }),
        new webpack.optimize.UglifyJsPlugin({
            banner,
            beautify: false,
            comments: false,
        }),
    ] : [
        // dev-only
        new webpack.EnvironmentPlugin({
            NODE_ENV: 'development',
            DEBUG: true,
        }),
        new webpack.DefinePlugin({
            'NODE_ENV': JSON.stringify('development'),
            'process.env': {
                NODE_ENV: 'development',
            },
        }),
    ]).concat([
        // dev-and-prod
        new webpack.optimize.CommonsChunkPlugin({
            name: 'vendor',
            filename: './assets/vendor.min.js',
        }),
        new ExtractTextPlugin({
            filename: './assets/app.min.css',
        }),
        new webpack.BannerPlugin({
            banner,
        }),
        new webpack.ProvidePlugin({
            $: 'jquery',
            jQuery: 'jquery',
        }),
    ]).concat(USE_LINTERS ? [
        new StyleLintPlugin({
            fix: true,
            files: ['**/*.scss'],
            syntax: 'scss',
        }),
    ] : []),

    devtool: USE_SOURCE_MAP ? 'eval-source-map' : '',

    module: {
        rules: [
            // javascript loaders
            {
                test: /\.js$/,
                include: /node_modules/,
                loader: 'imports-loader',
                options: {
                    $: 'jquery',
                    jQuery: 'jquery',
                },
            },
            {
                test: /\.js$/,
                exclude: /node_modules/,
                loaders: [
                    {
                        loader: 'babel-loader',
                        options: {
                            presets: ['env'],
                            forceEnv: NODE_ENV,
                            cacheDirectory: !PROD,
                        },
                    },
                ].concat(USE_LINTERS ? [
                    {
                        loader: 'eslint-loader',
                        options: {
                            fix: true,
                            cache: !PROD,
                        },
                    },
                ] : []),
            },
            // image loaders
            {
                test: /\.(jpe?g|png|gif|svg)$/i,
                exclude: /fonts/,
                loaders: [
                    {
                        loader: 'url-loader',
                        options: {
                            limit: 32 * 1024,
                        },
                    },
                    {
                        loader: 'file-loader',
                        options: {
                            name: `assets/images/[name].[ext]${PROD ? '?v=[hash]' : ''}`,
                        },
                    },
                    {
                        loader: 'image-webpack-loader',
                        options: {
                            bypassOnDebug: !PROD,
                        },
                    },
                ],
            },
            // font loaders
            {
                test: /\.(eot|woff|woff2|ttf|svg)(\?v=.+)?$/,
                loaders: [
                    {
                        loader: 'file-loader',
                        options: {
                            name: `assets/fonts/[name].[ext]${PROD ? '?v=[hash]' : ''}`,
                        },
                    },
                ],
            },
            // css loaders
            {
                test: /\.s?css$/,
                loader: ExtractTextPlugin.extract({
                    fallback: [
                        {
                            loader: 'style-loader',
                            options: {
                                sourceMap: USE_SOURCE_MAP,
                            },
                        },
                    ],
                    use: [
                        {
                            loader: 'css-loader',
                            options: {
                                importLoaders: 2, // index of 'sass-loader'
                                sourceMap: USE_SOURCE_MAP,
                            },
                        },
                        {
                            loader: 'postcss-loader',
                            options: {
                                sourceMap: USE_SOURCE_MAP ? 'inline' : false,
                                plugins: [
                                    // dev-and-prod
                                    require('postcss-cssnext')({
                                        features: {},
                                    }),
                                ].concat(PROD ? [
                                    // prod-only
                                    require('css-mqpacker')({
                                        sort: true,
                                    }),
                                    require('cssnano')({
                                        autoprefixer: false,
                                        discardComments: {
                                            removeAll: true,
                                        },
                                    }),
                                ] : [
                                    // dev-only
                                ]),
                            },
                        },
                        {
                            loader: 'sass-loader',
                            options: {
                                data: `$NODE_ENV: ${NODE_ENV};`,
                                indentWidth: 4,
                                sourceMap: USE_SOURCE_MAP ? 'inline' : false,
                                sourceMapEmbed: USE_SOURCE_MAP,
                                sourceMapContents: USE_SOURCE_MAP,
                                sourceMapRoot: '..',
                                sourceComments: USE_SOURCE_MAP,
                            },
                        },
                    ],
                }),
            },
        ],
    },

};
